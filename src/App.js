var knownEmoji = Object.keys(emojiDictionary);

export default function App() {
  const [emojiMeaning, setEmojiMeaning] = useState("");

  function emojiInputHandler(event) {
    var userInput = event.target.value;
    var emojiMeaning = emojiDictionary[userInput];

    if (emojiMeaning === undefined) {
      emojiMeaning = "Huh.. Haven't seen that one before.";
    }
    // console.log(emojiMeaning);
    setEmojiMeaning(emojiMeaning);
  }

  function emojiClickHandler(emoji) {
    var emojiMeaning = emojiDictionary[emoji];
    setEmojiMeaning(emojiMeaning);
  }

  return (
    <div className="App">
      <h1 style={{ color: "#1C6DD0" }}> ~Emoji Interpreter~ </h1>

      <input
        placeholder="Search the emoji here or pick one from below. "
        onChange={emojiInputHandler}
      />
      <h2 style={{ color: "#1C6DD0" }}> OUTPUT: {emojiMeaning} </h2>
      <h3 style={{ color: "darkCyan" }}> Emoji's We Know </h3>
      {knownEmoji.map(function (emoji) {
        return (
          <span
            onClick={() => emojiClickHandler(emoji)}
            style={{
              fontSize: "2rem",
              padding: "0.5rem 1rem",
              cursor: "pointer"
            }}
            key={emoji}
          >
            {" "}
            {emoji}{" "}
          </span>
        );
      })}
    </div>
  );
}
